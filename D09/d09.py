h_ = [0, 0]
ts_ = [[0, 0] for _ in range(9)]
tail_visited = {"0,0": True}  # First tail 9's position


def update_tails_position(r_, t_):
    # print(r_, t_)
    if t_[0] == r_[0]:
        if abs(r_[1] - t_[1]) > 1:
            t_[1] = r_[1] - 1 if r_[1] > t_[1] else r_[1] + 1
    elif t_[1] == r_[1]:
        if abs(r_[0] - t_[0]) > 1:
            t_[0] = r_[0] - 1 if r_[0] > t_[0] else r_[0] + 1
    else:  # Cas ou la formation est diagonale
        if (abs(r_[1] - t_[1]) + abs(r_[0] - t_[0])) > 2:
            t_[0] = t_[0] - 1 if r_[0] < t_[0] else t_[0] + 1
            t_[1] = t_[1] - 1 if r_[1] < t_[1] else t_[1] + 1

def update_tails():
    global h_, ts_
    update_tails_position(h_, ts_[0])
    for j_ in range(1, 9):
        update_tails_position(ts_[j_-1], ts_[j_])
    tail_visited[",".join([str(ts_[-1][0]), str(ts_[-1][1])])] = True

def update_full_postions(y_, x_):
    print(f"update_full_postions({y_}, {x_})")
    global h_, ts_
    for i_ in range(abs(y_)):
        h_ = [h_[0] + (1 if abs(y_) == y_ else -1), h_[1]]
        print(f"h_: {h_}")
        update_tails()
    for i_ in range(abs(x_)):
        h_ = [h_[0], h_[1] + (1 if abs(x_) == x_ else -1)]
        print(f"h_: {h_}")
        update_tails()
    print(f"ts_: {ts_}")


